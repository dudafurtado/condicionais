// explicando a funcionalidade do else depois do if

const temQueSerIgual = 17 == 17 
const naoEhDiferente = 34 != 34

if (temQueSerIgual == true) {
    console.log("Boa")
} else {
    console.log("Não ta boa")
}

if (naoEhDiferente !== false) {
    console.log("Divertido")
} else {
    console.log("Não é divertido")
}